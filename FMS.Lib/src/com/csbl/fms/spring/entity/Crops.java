
package com.csbl.fms.spring.entity;

public class Crops {
    private String oid;
    private String cropsId;
    private String cropsName;
    private String cropsDescription;

    public Crops() {
    }

    public String getOid() {
        return oid;
    }

    public void setOid(String oid) {
        this.oid = oid;
    }

    public String getCropsId() {
        return cropsId;
    }

    public void setCropsId(String cropsId) {
        this.cropsId = cropsId;
    }

    public String getCropsName() {
        return cropsName;
    }

    public void setCropsName(String cropsName) {
        this.cropsName = cropsName;
    }

    public String getCropsDescription() {
        return cropsDescription;
    }

    public void setCropsDescription(String cropsDescription) {
        this.cropsDescription = cropsDescription;
    }

    
    
    
}
